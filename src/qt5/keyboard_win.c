const uint8_t *
keyboard_map_key(uint32_t native_scancode)
{
	size_t k;
	
	for (k = 0; key_map[k].native_scancode != 0; k++) {
		if (key_map[k].native_scancode == native_scancode) {
			return key_map[k].set_2;
		}
	}
	return NULL;
}
